# metodos de cadenas

cadena1 = 'Hola Mundo'
print(f'Cadena original: {cadena1}')
mayusculas = cadena1.upper()
print(f'Cadena en mayusculas: {mayusculas}')
print(f'Cadena en minusculas: {cadena1.lower()}')
cadena2 = ' juan perez '
print(cadena2)
print(cadena2.strip())
cadena3 = 'prueba cadena'
print(cadena3.capitalize())
print(cadena3.endswith('ena'))
cadena4 = 'casa'
print(cadena4.ljust(10,'0'))
print(cadena3.partition(' '))
print(cadena1.swapcase())