print('*** Operadores de comparacion (Relacionales) ***')

a, b = 7, 5

# Operador igualdad ==
resultado = a == b
print(f'Resultado de a == b es : {resultado}')

# Operador diferente !=
resultado = a != b
print(f'Resultado de a != b es : {resultado}')

# Operador mayor que >
resultado = a > b
print(f'Resultado de a > b: {resultado}')