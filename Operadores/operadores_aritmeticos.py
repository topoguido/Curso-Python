# Operadores aritméticos

a = 10
b = 3

# Suma +
suma = a + b
print(f'Suma: {suma}')

# Resta -
resta = a - b
print(f'Resta: {resta}')

# Multiplicacion *
multiplicacion = a * b
print(f'Multiplicación: {multiplicacion}')

# Division / (retorna tipo float)
division = a / b
print(f'Division: {division:.2f}') #( :.2f  le indica que retorne float con dos decumales para esa variable)

# Division entera (//)
division_entera = a // b
print(f'Division entera: {division_entera}')

# Modulo (%) de la division
modulo = a % b
print(f'Residuo de la división: {modulo}')

# Exponente (potencia) **
exponente = a ** b
print(f'Exponente: {exponente}')
